//! Edge case tests per spec EC table and common attribution bypass attempts.
//!
//! EC-002: `git commit --allow-empty` with attribution → Block.
//! EC-003: Attribution in commit body (not subject line) → Block.
//! Additional: case-insensitivity, whitespace variants, embedded patterns,
//! partial matches that must NOT match.

use block_ai_attribution::{detect_attribution, on_hook_logic};
use vsdd_hook_sdk::{HookPayload, HookResult};

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

fn bash_payload(command: &str) -> HookPayload {
    let json = serde_json::json!({
        "event_name": "PreToolUse",
        "tool_name": "Bash",
        "session_id": "sess-ec",
        "dispatcher_trace_id": "trace-ec",
        "tool_input": { "command": command }
    });
    serde_json::from_value(json).expect("fixture must deserialize")
}

fn assert_block(result: HookResult, label: &str) {
    match result {
        HookResult::Block { .. } => {}
        other => panic!("{label}: expected Block, got {other:?}"),
    }
}

fn assert_continue(result: HookResult, label: &str) {
    match result {
        HookResult::Continue => {}
        other => panic!("{label}: expected Continue, got {other:?}"),
    }
}

// ---------------------------------------------------------------------------
// EC-002: git commit --allow-empty with attribution
// ---------------------------------------------------------------------------

/// EC-002: `git commit --allow-empty` + Co-Authored-By: Claude → Block.
#[test]
fn test_BC_4_05_003_ec002_allow_empty_with_coauthored_by_claude_is_blocked() {
    let cmd = "git commit --allow-empty -m \"chore: placeholder\n\nCo-Authored-By: Claude <noreply@anthropic.com>\"";
    let result = on_hook_logic(bash_payload(cmd));
    assert_block(
        result,
        "git commit --allow-empty with Co-Authored-By: Claude",
    );
}

/// EC-002: `git commit --allow-empty` + Generated with Claude Code → Block.
#[test]
fn test_BC_4_05_003_ec002_allow_empty_with_generated_with_claude_code_is_blocked() {
    let cmd = "git commit --allow-empty -m \"chore: placeholder\n\n🤖 Generated with Claude Code\"";
    let result = on_hook_logic(bash_payload(cmd));
    assert_block(
        result,
        "git commit --allow-empty with Generated with Claude Code",
    );
}

