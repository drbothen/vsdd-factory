/// "co-author" (human co-author, not AI) must not match.
#[test]
fn test_TV_EC_human_coauthor_does_not_match() {
    let cmd =
        "git commit -m \"feat: pair programmed\n\nCo-Authored-By: Alice <alice@example.com>\"";
    let result = detect_attribution(cmd);
    assert!(
        result.is_none(),
        "human Co-Authored-By should not be flagged; got {result:?}"
    );
}

/// Word "generated" without AI context must not match.
#[test]
fn test_TV_EC_word_generated_alone_does_not_match() {
    let cmd = "git commit -m \"chore: generated migrations from schema\"";
    let result = detect_attribution(cmd);
    assert!(
        result.is_none(),
        "'generated' without AI context must not match; got {result:?}"
    );
}

/// "claude" as part of a human name that is not an AI tool attribution.
#[test]
fn test_TV_EC_human_name_claude_in_human_context_does_not_match() {
    // "Claude Debussy" — human composer, not AI tool attribution.
    // This is a gray area; spec says "Co-Authored-By: Claude" triggers.
    // A human named "Claude" without "Anthropic" context and NOT in Co-Authored-By:
    // form should NOT trigger. This confirms the pattern is anchored to the
    // Co-Authored-By header or explicit "Generated by/with" phrases.
    let cmd = "git commit -m \"chore: inspired by Claude Debussy's approach\"";
    let result = detect_attribution(cmd);
    assert!(
        result.is_none(),
        "human reference to 'Claude' (not in attribution context) must not match; got {result:?}"
    );
}

