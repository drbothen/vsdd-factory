/// Pattern matching must be case-insensitive: all-caps variant.
#[test]
fn test_TV_EC_case_insensitive_uppercase_coauthored() {
    let cmd = "git commit -m \"fix\n\nCO-AUTHORED-BY: CLAUDE <NOREPLY@ANTHROPIC.COM>\"";
    let result = detect_attribution(cmd);
    assert!(
        result.is_some(),
        "uppercase CO-AUTHORED-BY must be detected (case-insensitive match)"
    );
}

/// Pattern matching must be case-insensitive: mixed-case Generated.
#[test]
fn test_TV_EC_case_insensitive_mixed_case_generated_by_claude() {
    let cmd = "git commit -m \"fix\n\ngenerated BY claude\"";
    let result = detect_attribution(cmd);
    assert!(
        result.is_some(),
        "mixed-case 'generated BY claude' must be detected"
    );
}

