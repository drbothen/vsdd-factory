/// EC-003: Attribution only in body paragraph (not the first line) → Block.
#[test]
fn test_BC_4_05_003_ec003_attribution_in_body_not_subject_is_blocked() {
    let cmd = concat!(
        "git commit -m \"fix: correct panic in parser\n\n",
        "This patch addresses the root cause.\n\n",
        "Co-Authored-By: Claude <noreply@anthropic.com>\""
    );
    let result = on_hook_logic(bash_payload(cmd));
    assert_block(result, "attribution buried in commit body");
}

/// EC-003: `Generated by AI` in body only → Block.
#[test]
fn test_BC_4_05_003_ec003_generated_by_ai_in_body_is_blocked() {
    let cmd = "git commit -m \"docs: update readme\n\nContent generated by AI for review.\"";
    let result = on_hook_logic(bash_payload(cmd));
    assert_block(result, "Generated by AI in body");
}

