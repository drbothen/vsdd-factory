/// EC-002 full payload path: Block result carries a reason mentioning the pattern.
#[test]
fn test_BC_4_05_003_block_reason_mentions_pattern_for_ec002() {
    let cmd =
        "git commit --allow-empty -m \"chore\n\nCo-Authored-By: Claude <noreply@anthropic.com>\"";
    let result = on_hook_logic(bash_payload(cmd));
    match result {
        HookResult::Block { reason } => {
            assert!(!reason.is_empty(), "Block reason must be non-empty");
        }
        other => panic!("Expected Block, got {other:?}"),
    }
}

/// `git commit -F file` pattern — if the command string contains -F,
/// the command itself is still inspected for inline attribution.
/// A -F invocation without inline attribution in the command string → Continue.
#[test]
fn test_TV_EC_git_commit_file_flag_without_inline_attribution_returns_continue() {
    let cmd = "git commit -F /tmp/commit-msg.txt";
    let result = on_hook_logic(bash_payload(cmd));
    assert_continue(
        result,
        "git commit -F with no inline attribution text in command",
    );
}
